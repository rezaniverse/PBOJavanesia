public class getKelilingLingkaran {
    public static void main(String[] args) {
        System.out.println("-> Program java untuk mencari keliling lingkaran <-");
        
        float pi = 3.14f; 
        double diameter = 10;

        double keliling = pi * diameter;
        System.out.println("Keliling lingkaran dengan diameter " + diameter + " adalah: " + keliling);
    }
}
